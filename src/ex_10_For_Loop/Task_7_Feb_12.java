package ex_10_For_Loop;

public class Task_7_Feb_12 {
    public static void main(String[] args) {
        //right angle triangle
        //******
        //*****
        //***
        //**
        // *


        for(int i =0; i<=5; i++)
        {
            for(int j=0; j<=i; j++) {

                System.out.print("*");
            }
            System.out.println();
        }

    }
}
